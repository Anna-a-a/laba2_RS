#!/bin/bash

echo "Тестирование производительности двумерных массивов с разным количеством потоков"
echo "============================================================================"

# Количество потоков для тестирования
threads=(1 2 4 8 16)

# Запуск последовательной версии
echo -e "\nПоследовательная версия:"
./sequential_2d_arrays

# Запуск параллельной версии с разным количеством потоков
for thread in "${threads[@]}"; do
    echo -e "\nПараллельная версия с $thread потоками:"
    export OMP_NUM_THREADS=$thread
    ./parallel_2d_arrays $thread
done 